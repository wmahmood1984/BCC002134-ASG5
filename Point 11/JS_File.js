var char = prompt("please enter vowel")
switch(char){
    case "A":
    console.log("A is a vowel");
    break;
    case "E":
    console.log("E is a vowel");
    break;
    case "I":
    console.log("I is a vowel");
    break;
    case "O":
    console.log("O is a vowel");
    break;
    case "U":
    console.log("U is a vowel");
    break;
    default:
        console.log("not vowel")
        break;    
}